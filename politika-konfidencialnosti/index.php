<ol>
<li>Оператор обеспечивает сохранность персональных данных и&nbsp;принимает все&nbsp;возможные меры, исключающие доступ к&nbsp;персональным данным неуполномоченных лиц.</li>
<li>Персональные данные Пользователя никогда,&nbsp;ни&nbsp;при&nbsp;каких&nbsp;условиях не&nbsp;будут переданы третьим лицам, за&nbsp;исключением случаев, связанных с&nbsp;исполнением действующего законодательства.</li>
<li>В&nbsp;случае выявления неточностей в&nbsp;персональных данных, Пользователь может актуализировать их&nbsp;самостоятельно, путем направления Оператору уведомление на&nbsp;адрес электронной почты Оператора <a href="ma&#105;&#76;&#84;&#111;&#58;av&#64;misyuchenko&#46;ru" title="">av&#64;misyuchenko&#46;ru</a> с&nbsp;пометкой «Актуализация персональных данных».</li>
