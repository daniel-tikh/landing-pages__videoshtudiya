<ol>
<li>Оператор обрабатывает персональные данные Пользователя только в&nbsp;случае их&nbsp;заполнения и/или&nbsp;отправки Пользователем самостоятельно через&nbsp;специальные формы, расположенные на&nbsp;сайте <a href="/" title="">https://видеоштудия.рф</a>. Заполняя соответствующие формы и/или&nbsp;отправляя свои&nbsp;персональные данные Оператору, Пользователь выражает своё&nbsp;согласие с&nbsp;данной Политикой.</li>
<li>Оператор обрабатывает обезличенные данные о&nbsp;Пользователе в&nbsp;случае, если&nbsp;это&nbsp;разрешено в&nbsp;настройках браузера Пользователя (включено сохранение файлов «cookie» и&nbsp;использование технологии JavaScript).</li>
</ol>

</li>

