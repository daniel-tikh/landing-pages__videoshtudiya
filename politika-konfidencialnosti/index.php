<ol>
<li>Цель обработки персональных данных Пользователя &mdash;&nbsp;заключение, исполнение и&nbsp;прекращение <span class="nobr">гражданско-правовых</span> договоров; предоставление доступа Пользователю к&nbsp;сервисам, информации и/или&nbsp;материалам, содержащимся <span class="nobr">на&nbsp;веб-сайте</span> <a href="/" title="">https://видеоштудия.рф</a>; уточнение деталей заказа.</li>
<li>Также&nbsp;Оператор имеет право направлять Пользователю уведомления о&nbsp;новых продуктах и&nbsp;услугах, специальных предложениях и&nbsp;различных событиях. Пользователь всегда&nbsp;может отказаться от&nbsp;получения информационных сообщений, направив Оператору письмо на&nbsp;адрес электронной почты <a href="ma&#105;&#76;&#84;&#111;&#58;av&#64;misyuchenko&#46;ru" title="">av&#64;misyuchenko&#46;ru</a> с&nbsp;пометкой «Отказ от&nbsp;уведомлениях о&nbsp;новых продуктах и&nbsp;услугах и&nbsp;специальных предложениях».</li>
<li>Обезличенные данные Пользователей, собираемые с&nbsp;помощью сервисов <span class="nobr">интернет-статистики,</span> служат для&nbsp;сбора информации о&nbsp;действиях Пользователей на&nbsp;сайте, улучшения качества сайта и&nbsp;его&nbsp;содержания.</li>
</ol>

</li>

