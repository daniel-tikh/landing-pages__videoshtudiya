<p><strong>Политика в&nbsp;отношении обработки персональных данных</strong>

<ol class="notab">

<li><strong>Общие положения</strong><br /><br />Настоящая политика обработки персональных данных составлена в&nbsp;соответствии с&nbsp;требованиями Федерального закона от&nbsp;27.07.2006&nbsp;г. <span class="nobr">№&nbsp;152-ФЗ&nbsp;«О&nbsp;персональных</span> данных» и&nbsp;определяет порядок обработки персональных данных и&nbsp;меры по&nbsp;обеспечению безопасности персональных данных Индивидуальный предприниматель Мисюченко Андрей Владимирович (далее &mdash;&nbsp;Оператор).

<ol>
<li>Оператор ставит своей&nbsp;важнейшей целью и&nbsp;условием осуществления своей&nbsp;деятельности соблюдение прав и&nbsp;свобод человека и&nbsp;гражданина при&nbsp;обработке его&nbsp;персональных данных, в&nbsp;том&nbsp;числе защиты прав на&nbsp;неприкосновенность частной жизни, личную и&nbsp;семейную тайну.</li>
<li>Настоящая политика Оператора в&nbsp;отношении обработки персональных данных (далее &mdash;&nbsp;Политика) применяется ко&nbsp;всей&nbsp;информации, которую&nbsp;Оператор может получить о&nbsp;посетителях <span class="nobr">веб-сайта</span> <a href="/" title="">https://видеоштудия.рф</a>.</li>
</ol>

</li>

<li><strong>Основные понятия, используемые в&nbsp;Политике</strong>

<ol>
<li>Автоматизированная обработка персональных данных &mdash;&nbsp;обработка персональных данных с&nbsp;помощью средств вычислительной техники;</li>
<li>Блокирование персональных данных &mdash;&nbsp;временное прекращение обработки персональных данных (за&nbsp;исключением случаев, если&nbsp;обработка необходима для&nbsp;уточнения персональных данных);</li>
<li>Веб-сайт &mdash;&nbsp;совокупность графических и&nbsp;информационных материалов, а&nbsp;также&nbsp;программ для&nbsp;ЭВМ и&nbsp;баз данных, обеспечивающих их&nbsp;доступность в&nbsp;сети интернет по&nbsp;сетевому адресу <a href="/" title="">https://видеоштудия.рф</a>;</li>
<li>Информационная система персональных данных &mdash;&nbsp;совокупность содержащихся в&nbsp;базах данных персональных данных, и&nbsp;обеспечивающих их&nbsp;обработку информационных технологий и&nbsp;технических средств;</li>
<li>Обезличивание персональных данных &mdash;&nbsp;действия, в&nbsp;результате которых&nbsp;невозможно определить без&nbsp;использования дополнительной информации принадлежность персональных данных конкретному Пользователю или&nbsp;иному субъекту персональных данных;</li>
<li>Обработка персональных данных &mdash;&nbsp;любое действие (операция) или&nbsp;совокупность действий (операций), совершаемых с&nbsp;использованием средств автоматизации или&nbsp;без&nbsp;использования таких&nbsp;средств с&nbsp;персональными данными, включая сбор, запись, систематизацию, накопление, хранение, уточнение (обновление, изменение), извлечение, использование, передачу (распространение, предоставление, доступ), обезличивание, блокирование, удаление, уничтожение персональных данных;</li>
<li>Оператор &mdash;&nbsp;государственный орган, муниципальный орган, юридическое или&nbsp;физическое лицо, самостоятельно или&nbsp;совместно с&nbsp;другими лицами организующие и&nbsp;(или)&nbsp;осуществляющие обработку персональных данных, а&nbsp;также&nbsp;определяющие цели обработки персональных данных, состав персональных данных, подлежащих обработке, действия (операции), совершаемые с&nbsp;персональными данными;</li>
<li>Персональные данные &mdash;&nbsp;любая информация, относящаяся прямо или&nbsp;косвенно к&nbsp;определенному или&nbsp;определяемому Пользователю <span class="nobr">веб-сайта</span> <a href="/" title="">https://видеоштудия.рф</a>;</li>
<li>Пользователь &mdash;&nbsp;любой посетитель <span class="nobr">веб-сайта</span> <a href="/" title="">https://видеоштудия.рф</a>;</li>
<li>Предоставление персональных данных &mdash;&nbsp;действия, направленные на&nbsp;раскрытие персональных данных определенному лицу или&nbsp;определенному кругу лиц;</li>
<li>Распространение персональных данных &mdash;&nbsp;любые действия, направленные на&nbsp;раскрытие персональных данных неопределенному кругу лиц (передача персональных данных) или&nbsp;на&nbsp;ознакомление с&nbsp;персональными данными неограниченного круга лиц, в&nbsp;том&nbsp;числе обнародование персональных данных в&nbsp;средствах массовой информации, размещение в&nbsp;информационно-телекоммуникационных сетях или&nbsp;предоставление доступа к&nbsp;персональным данным <span class="nobr">каким-либо&nbsp;иным</span> способом;</li>
<li>Уничтожение персональных данных &mdash;&nbsp;любые действия, в&nbsp;результате которых&nbsp;персональные данные уничтожаются безвозвратно с&nbsp;невозможностью дальнейшего восстановления содержания персональных данных в&nbsp;информационной системе персональных данных и&nbsp;(или)&nbsp;результате которых&nbsp;уничтожаются материальные носители персональных данных.</li>
</ol>

</li>

<li><strong>Оператор может обрабатывать следующие персональные данные Пользователя</strong>

<ol>
<li>Фамилия, имя, отчество.</li>
<li>Электронный адрес.</li>
<li>Номера телефонов.</li>
